    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String categoryID = request.getParameter("cid");
        ProductDAO pDAO = new ProductDAO();
        List<Product> productList = pDAO.getProductsByCid(categoryID);
        for (Product product : productList) {
            System.out.println(product.toString());
        }

        String name = request.getParameter("pid");
        String code = request.getParameter("code");
        String seriNumber = request.getParameter("seriNumber");
        Repository repo = new Repository();
        repo.setProductId(name);
        repo.setCode(code);
        repo.setSeriNumber(seriNumber);
        RepositoryDAO repoDAO = new RepositoryDAO();
        boolean insertNewCard = repoDAO.insertNewCard(repo);
        System.out.println("insertNewCard" + insertNewCard);
        String referer = request.getHeader("referer");
        response.sendRedirect(referer);
    }

